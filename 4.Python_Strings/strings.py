# The + Operator
x = "Trump is"
y = " a President"
z = " a Clown"

x + y            # Returns 'Trump is a President'
x + z            # Returns 'Trump is a Clown' 

# The * Operator
s = "eat.."
s * 4            # Returns 'eat..eat..eat..eat..'

# The in Operator
'a' in 'aeiou'   # Returns True
'z' in 'aeiou'   # Returns False

'land' in 'a good spaceous land'    # Returns True
'grass' in 'a good spaceous land'   # Returns False

# not in
'bad' not in 'a good spaceous land' # Returns True